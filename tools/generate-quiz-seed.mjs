/**
 * One-off: reads js/data/quiz-data.js and writes database/seeds/quiz_questions_from_quiz_data.sql
 * Run: node tools/generate-quiz-seed.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const quizPath = path.join(root, 'js', 'data', 'quiz-data.js');
const txt = fs.readFileSync(quizPath, 'utf8');

const start = txt.indexOf('const QUIZ_DATA = ');
if (start === -1) throw new Error('QUIZ_DATA not found');
const after = txt.slice(start + 'const QUIZ_DATA = '.length);
let depth = 0;
let end = -1;
for (let i = 0; i < after.length; i++) {
  const c = after[i];
  if (c === '{') depth++;
  if (c === '}') {
    depth--;
    if (depth === 0) {
      end = i + 1;
      break;
    }
  }
}
if (end === -1) throw new Error('Could not find end of QUIZ_DATA');
const QUIZ_DATA = (0, eval)('(' + after.slice(0, end) + ')');

function esc(s) {
  return String(s).replace(/\\/g, '\\\\').replace(/'/g, "''");
}

const lines = [];
lines.push('-- Generated by tools/generate-quiz-seed.mjs from js/data/quiz-data.js');
lines.push('USE adb_learning_platform;');
lines.push('');

for (const [topicKey, questions] of Object.entries(QUIZ_DATA)) {
  if (!Array.isArray(questions)) continue;
  for (const q of questions) {
    if (!q || !q.q || !q.options || q.options.length < 4) continue;
    const opts = q.options;
    const letter = ['A', 'B', 'C', 'D'][q.correct] ?? 'A';
    lines.push(
      'INSERT INTO quiz_questions (topic_id, question_text, option_a, option_b, option_c, option_d, correct_option, explanation, question_type)'
    );
    lines.push(
      "SELECT t.topic_id, '" +
        esc(q.q) +
        "', '" +
        esc(opts[0]) +
        "', '" +
        esc(opts[1]) +
        "', '" +
        esc(opts[2]) +
        "', '" +
        esc(opts[3]) +
        "', '" +
        letter +
        "', NULL, 'mcq' FROM topics t WHERE t.topic_code = '" +
        esc(topicKey) +
        "' AND NOT EXISTS ("
    );
    lines.push("  SELECT 1 FROM quiz_questions q WHERE q.topic_id = t.topic_id AND q.question_text = '" + esc(q.q) + "'");
    lines.push(');');
    lines.push('');
  }
}

const outDir = path.join(root, 'database', 'seeds');
fs.mkdirSync(outDir, { recursive: true });
const outFile = path.join(outDir, 'quiz_questions_from_quiz_data.sql');
fs.writeFileSync(outFile, lines.join('\n'), 'utf8');
const inserts = lines.filter((l) => l.startsWith('INSERT INTO')).length;
console.log('Wrote', outFile, 'insert blocks:', inserts);
